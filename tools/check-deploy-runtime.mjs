#!/usr/bin/env node
/**
 * Deploy runtime guard.
 * Checks that the production deploy manifest stays public/runtime-only and packable.
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT_DIR = path.resolve(__dirname, '..');
const MANIFEST_PATH = path.join(ROOT_DIR, '.deploy/include-files.txt');

const ALLOWED_PRIVATE_EXCEPTIONS = new Set([
  '.deploy/include-files.txt',
  'tools/generate-public-file-list.mjs',
]);
const REQUIRED_RUNTIME_FILES = [
  'index.html',
  'parokonvektomaty.html',
  'parokonvektomat-rational.html',
  'parokonvektomat-unox.html',
  'analytics.js',
  'telegram-form.js',
  'main.js',
  'server/telegram-api.mjs',
  'data/runtime-config.json',
  'robots.txt',
  'sitemap.xml',
  'version.json',
];
const FORBIDDEN_PREFIXES = [
  '.ai/',
  '.git/',
  'docs/',
  'reports/',
  'src/',
  'node_modules/',
  'build/',
];

function fail(message) {
  console.error(`❌ ${message}`);
  process.exitCode = 1;
}

function ok(message) {
  console.log(`✅ ${message}`);
}

function readManifest() {
  if (!fs.existsSync(MANIFEST_PATH)) {
    fail('missing .deploy/include-files.txt');
    return [];
  }
  return fs.readFileSync(MANIFEST_PATH, 'utf8')
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter(Boolean)
    .filter((line) => !line.startsWith('#'));
}

console.log('\n# Deploy runtime guard');
const files = readManifest();
const unique = new Set(files);

if (files.length !== unique.size) fail(`manifest has duplicate entries: ${files.length - unique.size}`);
else ok(`manifest unique entries (${files.length})`);

for (const file of REQUIRED_RUNTIME_FILES) {
  if (!unique.has(file)) fail(`manifest missing required runtime file: ${file}`);
}
ok('required runtime files are listed');

for (const file of files) {
  if (path.isAbsolute(file) || file.includes('..')) {
    fail(`manifest has unsafe path: ${file}`);
    continue;
  }

  if (!ALLOWED_PRIVATE_EXCEPTIONS.has(file)) {
    const forbidden = FORBIDDEN_PREFIXES.find((prefix) => file.startsWith(prefix));
    if (forbidden) fail(`manifest includes forbidden private path: ${file}`);
  }

  const abs = path.join(ROOT_DIR, file);
  if (file === 'version.json' && !fs.existsSync(abs)) {
    ok('version.json is generated by deploy:pack');
    continue;
  }
  if (!fs.existsSync(abs)) fail(`manifest references missing file: ${file}`);
}

const htmlFiles = files.filter((file) => file.endsWith('.html'));
if (htmlFiles.length < 30) fail(`unexpectedly low HTML page count in deploy manifest: ${htmlFiles.length}`);
else ok(`HTML pages in manifest (${htmlFiles.length})`);

const runtimeConfigPath = path.join(ROOT_DIR, 'data/runtime-config.json');
try {
  const runtime = JSON.parse(fs.readFileSync(runtimeConfigPath, 'utf8'));
  if (runtime.telegramFormEndpoint !== '/api/send-telegram') fail('runtime-config: telegramFormEndpoint must be /api/send-telegram');
  else ok('runtime-config: telegram endpoint');
} catch (error) {
  fail(`runtime-config parse error: ${error.message}`);
}

const robots = fs.readFileSync(path.join(ROOT_DIR, 'robots.txt'), 'utf8');
if (!/Sitemap:\s*https:\/\/mospochin\.ru\/sitemap\.xml/i.test(robots)) fail('robots.txt missing production sitemap link');
else ok('robots.txt production sitemap link');

const sitemap = fs.readFileSync(path.join(ROOT_DIR, 'sitemap.xml'), 'utf8');
if (!sitemap.includes('https://mospochin.ru/parokonvektomaty.html')) fail('sitemap.xml missing parokonvektomaty.html');
else ok('sitemap.xml includes parokonvektomaty.html');

if (process.exitCode) {
  console.error('\n❌ Deploy runtime guard failed');
  process.exit(process.exitCode);
}
console.log('\n✅ Deploy runtime guard passed');
