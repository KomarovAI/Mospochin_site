#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { spawnSync } from 'node:child_process';

const root = process.cwd();
const node = process.execPath;
const npm = process.platform === 'win32' ? 'npm.cmd' : 'npm';

function parseArgs(argv) {
  const result = { _: [] };
  for (let index = 0; index < argv.length; index += 1) {
    const token = argv[index];
    if (!token.startsWith('--')) {
      result._.push(token);
      continue;
    }
    const key = token.slice(2);
    const next = argv[index + 1];
    if (!next || next.startsWith('--')) {
      result[key] = true;
      continue;
    }
    result[key] = next;
    index += 1;
  }
  return result;
}

function requireArg(args, key) {
  const value = args[key];
  if (!value || value === true) throw new Error(`Missing required argument --${key}`);
  return String(value).trim();
}

function run(label, command, args) {
  console.log(`\n# ${label}\n$ ${command} ${args.join(' ')}`);
  const result = spawnSync(command, args, { cwd: root, stdio: 'inherit', env: process.env });
  if (result.status !== 0) throw new Error(`${label} failed with status ${result.status ?? 1}`);
}

function forwardScaffoldArgs(argv) {
  const forwarded = [];
  for (let index = 0; index < argv.length; index += 1) {
    if (argv[index] === '--branch') {
      index += 1;
      continue;
    }
    forwarded.push(argv[index]);
  }
  return forwarded;
}

function pageFile(args) {
  return String(args.page || `${requireArg(args, 'slug')}.html`).trim();
}

function assertPage(file) {
  if (!/^[-a-z0-9]+\.html$/i.test(file)) throw new Error(`Invalid --page: ${file}`);
  if (!fs.existsSync(path.join(root, file))) throw new Error(`Page does not exist: ${file}`);
}

function createPage(args, rawArgv) {
  const branch = requireArg(args, 'branch');
  const scaffold = branch === 'restaurant'
    ? 'tools/scaffold-restaurant-service.mjs'
    : branch === 'household'
      ? 'tools/scaffold-household-service.mjs'
      : null;
  if (!scaffold) throw new Error(`Unsupported --branch ${branch}; use restaurant or household`);

  const file = pageFile(args);
  run(`scaffold ${branch}`, node, [scaffold, ...forwardScaffoldArgs(rawArgv)]);
  assertPage(file);
  run(`${branch} fallback sync`, npm, ['run', `${branch}:sync-fallbacks`, '--', '--page', file]);
  run('apply metrics page context', node, ['tools/apply-metrics-page-markup.mjs']);
  run('refresh AI page index', node, ['tools/generate-ai-index.mjs']);
  run('bootstrap new page source', node, ['tools/site-builder-bootstrap.mjs', '--pages', file]);
  run('sync builder manifest', node, ['tools/sync-site-builder-manifest.mjs']);
  run('build new page', node, ['tools/build-site.mjs', '--page', file, '--write']);
  run('sync metadata', node, ['tools/sync-page-metadata.mjs']);
  run('generate sitemap', node, ['tools/generate-sitemap.mjs']);
  run('generate deploy manifest', node, ['tools/generate-deploy-manifest.mjs']);
  run('refresh AI page index', node, ['tools/generate-ai-index.mjs']);
  run('refresh AI component map', node, ['tools/generate-ai-component-map.mjs']);
  run('refresh source complexity', node, ['tools/analyze-source-complexity.mjs']);
  run('refresh AI digest', node, ['tools/generate-ai-digest.mjs']);
  run('check new page', node, ['tools/doctor-page.mjs', '--page', file]);
  run('check full core contract', node, ['tools/check-profile.mjs', 'core']);
  console.log(`\nPage lifecycle complete: ${file}`);
}

function editPage(args) {
  const file = pageFile(args);
  assertPage(file);
  console.log(`Page edit surface: ${file}`);
  console.log(`Source: src/pages/${file.replace(/\.html$/, '')}/page.json and sections/`);
  console.log('Shared components: src/components/shared/ or src/components/parametric/');
  console.log('Metadata: data/page-metadata.json');
  console.log('Metrics context: data/metrics-page-context.json (generated by apply:metrics-markup)');
  run('page AI context', node, ['tools/ai-context.mjs', '--page', file]);
  run('page preflight', node, ['tools/inspect-page.mjs', '--page', file]);
}

function checkPage(args) {
  const file = pageFile(args);
  assertPage(file);
  run('builder parity', node, ['tools/build-site.mjs', '--page', file, '--check']);
  run('page doctor', node, ['tools/doctor-page.mjs', '--page', file]);
  run('metrics markup contract', node, ['tools/check-metrics-markup.mjs']);
  console.log(`\nPage checks passed: ${file}`);
}

try {
  const rawArgv = process.argv.slice(2);
  const command = rawArgv.shift() || 'help';
  const args = parseArgs(rawArgv);
  if (command === 'create') createPage(args, rawArgv);
  else if (command === 'edit') editPage(args);
  else if (command === 'check') checkPage(args);
  else {
    console.log('Usage:\n  npm run page:create -- --branch <restaurant|household> --slug <slug> ...\n  npm run page:edit -- --page <page.html>\n  npm run page:check -- --page <page.html>');
    process.exit(command === 'help' ? 0 : 2);
  }
} catch (error) {
  console.error(`Page lifecycle failed: ${error.message}`);
  process.exit(1);
}
